        <h2 class="text-2xl font-semibold mb-4">How to Write Using Markdown</h2>
        <p class="mb-4">Markdown is a simple way to format your writing. You don’t need to learn complicated tools — just a few easy tricks:</p>

        <ul class="list-disc list-inside ml-4 space-y-2">
            <li><strong># Heading</strong> → Use <code>#</code> at the start of a line for titles (add more <code>#</code> for smaller headings)</li>
            <li><strong>**Bold**</strong> → Wrap words with <code>**double stars**</code> for bold text</li>
            <li><strong>*Italic*</strong> → Use <code>*single stars*</code> for italics</li>
            <li><strong>- Bullet List</strong> → Start lines with a dash <code>-</code> to create bullet points</li>
            <li><strong>> Quote</strong> → Use <code>> </code> to highlight Bible verses or quotes</li>
            <li><strong>[Link](https://example.com)</strong> → Create links like this</li>
        </ul>

        <div class="mt-6 p-4 border border-dashed rounded-lg bg-zinc-100 dark:bg-zinc-800">
            <p class="font-medium mb-2">Example:</p>
            <pre class="text-sm whitespace-pre-wrap bg-zinc-50 dark:bg-zinc-900 p-3 rounded">
# My Testimony

I want to share what **Jesus** has done in my life.

## A New Beginning

> “Therefore, if anyone is in Christ, he is a new creation...” — 2 Corinthians 5:17

- I was lost but now am found
- God gave me peace and purpose

*To God be the glory!*
            </pre>
